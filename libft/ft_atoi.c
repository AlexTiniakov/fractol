int		ft_atoi(const char *str)
{
	unsigned long int nb[2];

	nb[0] = 0;
	nb[1] = 0;
	while (*str && ((*str >= 9 && *str <= 13) || *str == ' '))
		str++;
	if (*str != '-' && *str != '+' && (*str < '0' || *str > '9'))
		return (0);
	if (*str == '-')
	{
		nb[1] = 1;
		str++;
	}
	else if (*str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		nb[0] *= 10;
		nb[0] += (int)*str++ - '0';
		if (nb[0] >= 9223372036854775807 && nb[1] == 0)
			return (-1);
		if (nb[0] > 9223372036854775807 && nb[1] == 1)
			return (0);
	}
	return ((nb[1] == 1) ? (int)(-nb[0]) : (int)(nb[0]));
}
